public class Identifier {
    /**
     * 标识符：用于给程序中变量，类，方法命名的符号。
     * 命名规则：1.可以由字母，数字，_，和$组成，但是数字不能开头。
     *         2.不能是Java关键字和保留字，但是可以包含。
     *         3.不能空格。
     *         4.只能包含美元符$,不能包含@，#等特殊符号
     */

    public static void main(String[] args) {
        String $ = "dasda";
        System.out.println($);

        /*String 888 = 112;*/
    }
}
